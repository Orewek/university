#ifndef GLOBAL_VAR_H
#define GLOBAL_VAR_H

// координаты (массивы от числа частиц, где i – номер частицы)
// coordx[i], coordy[i], coordz[i]: Координаты i-ой частицы по осям X, Y, Z
double *coordx = nullptr, *coordy = nullptr, *coordz = nullptr; // на бумажке: rx[i], ry[i], rz[i]

// скорости  (массивы от числа частиц, где i – номер частицы)
// vx[i], vy[i], vz[i]: Компоненты скорости i-ой частицы по осям X, Y, Z
double *vx = nullptr, *vy = nullptr, *vz = nullptr;

// потенциальная, кинетическая, тепловая, полная, внутренняя энергии (скаляры)
/*
U: Потенциальная энергия всей системы (сумма по всем парам частиц)
Ekin: Кинетическая энергия всей системы
Eterm: Тепловая энергия (энергия относительно центра масс)
E: Полная энергия системы (E = Ekin + U)
Eint: Внутренняя энергия (Eint = Eterm + U)
*/
double U, Ekin, Eterm, E, Eint;

// силы взаимодействия
// Fx[i], Fy[i], Fz[i]: Компоненты силы, действующей на i-ую частицу
// F: Модуль силы между двумя частицами (временная переменная)
double *Fx = nullptr, *Fy = nullptr, *Fz = nullptr, F;

// Энергии (ср. на 1 ч-цу, на шаг)
// U1, Ekin1, E1, Eint1: Энергии на одну частицу на текущем шаге
// U1_av, Eterm1_av, E1_av, Eint1_av: Усредненные по времени энергии на одну частицу
double U1, Ekin1, E1, Eint1, U1_av, Eterm1_av, E1_av, Eint1_av;

//Температура
// T: Мгновенная температура системы (рассчитывается из тепловой энергии)
// T_av: Усредненная по времени температура
double T, T_av;

//Давление
/*
P: Полное давление системы
P1: Идеальная составляющая давления (от теплового движения)
P2: Вирьяльная составляющая давления (от межчастичных взаимодействий)
P_av: Усредненное по времени давление
*/
double P, P1, P2, P_av;

#endif 
