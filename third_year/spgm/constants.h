#ifndef CONSTANS_H
#define CONSTANS_H
#include "params.h"
#include <math.h>

// Номер Варианта
const double n = 19;
// Масса одной частицы (атома Ar) в атомных единицах массы (66.335 а.е.м. ≈ 1.66×10⁻²⁷ кг)
 const double MASS = n * 66.335;
// Постоянная Больцмана в СИ (1.380648528×10⁻²³ Дж/К) связывает температуру с энергией
 const double K_B = 1.380648528;
// Параметр энергии. Глубина потенциальной ямы (1.712 кДж/моль) - характеризует силу притяжения между атомам
 const double EPS = pow(2, 1.0 / n) * 1.712;
// Расстояние, на котором потенциал равен нулю (0.3418 нм) - характерный размер атома
 const double SIGMA = 0.3418;
// Радиус обрезания (2.5×σ ≈ 0.8545 нм) - дальше этого расстояния взаимодействием пренебрегаем
const double RCUT = 2.5 * SIGMA; 
// Квадрат радиуса обрезания - используется для оптимизации вычислений (избегаем вычисления квадратного корня)
const double RCUT2 = RCUT * RCUT;
/*
Потенциал Леннард-Джонса
Значение потенциала на радиусе обрезания - используется для сдвига потенциала чтобы он плавно обрезался до нуля
U(r) = 4ε[(σ/r)¹² - (σ/r)⁶]
ε (EPS) - глубина потенциальной ямы
σ (SIGMA) - расстояние, где потенциал = 0
r - расстояние между частицами
*/
const double UCUT = 4 * EPS * (pow((SIGMA / RCUT), 12) - pow((SIGMA / RCUT), 6));

// Длина ребра эл. ячейки; зависит от термодинамического состояния вещества
// Параметр решетки (1.0 нм) - расстояние между атомами в кристаллической решетке
const double ACRIST = 1.0;

// размеры системы по осям координат
// Размеры simulation box по осям (2×1.0 = 2.0 нм) - определяют объем системы
const double LX = (double)NUMCRIST_X * ACRIST;
const double LY = (double)NUMCRIST_Y * ACRIST;
const double LZ = (double)NUMCRIST_Z * ACRIST;
// Объём системы
//  Объем системы (2×2×2 = 8 нм³) - используется для расчета давления
const double VOLUME = LX * LY * LZ;


#endif